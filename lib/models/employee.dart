class Employee {
  final String nombres;
  final String apellidos;
  final String email;
  final String telefono;
  final String role;
  final String fechaIngreso;

  Employee({
    required this.nombres,
    required this.apellidos,
    required this.email,
    required this.telefono,
    required this.role,
    required this.fechaIngreso,
  });
}